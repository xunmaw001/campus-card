import { isNumber,isIntNumer,isEmail,isPhone, isMobile,isURL,checkIdCard } from "@/utils/validate";
export default {
	data() {
		let self = this
		var validateIdCard = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!checkIdCard(value)) {
				callback(new Error("请输入正确的身份证号码"));
			} else {
				callback();
			}
		};
		var validateUrl = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isURL(value)) {
				callback(new Error("请输入正确的URL地址"));
			} else {
				callback();
			}
		};
		var validateMobile = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isMobile(value)) {
				callback(new Error("请输入正确的手机号码"));
			} else {
				callback();
			}
		};
		var validatePhone = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isPhone(value)) {
				callback(new Error("请输入正确的电话号码"));
			} else {
				callback();
			}
		};
		var validateEmail = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isEmail(value)) {
				callback(new Error("请输入正确的邮箱地址"));
			} else {
				callback();
			}
		};
		var validateNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isNumber(value)) {
				callback(new Error("请输入数字"));
			} else {
				callback();
			}
		};
		var validateIntNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isIntNumer(value)) {
				callback(new Error("请输入整数"));
			} else {
				callback();
			}
		};
		return {
			id: '',
			type: '',
			
			
			ro:{
				huiyimingcheng : false,
				xuehao : false,
				xingming : false,
				qiandaoshuoming : false,
				qiandaoshijian : false,
				longitude : false,
				latitude : false,
				fulladdress : false,
			},
			
			// 地图
			mapVisiable: false,
			zoom: 12,
			center: [116.410426, 39.934946],
			markers: [],
			searchOption: {
				citylimit: false
			},
            plugin: [{
                // 定位城市
                pName: "CitySearch",
                events: {
                    init(o) {
                        // o是高德地图定位插件实例
                        o.getLocalCity(function(status, result) {
                            if (status === "complete" && result.info === "OK") {
                                // 查询成功，result即为当前所在城市信息
                                self.center = [result.rectangle.split(';')[0].split(',')[0], result.rectangle.split(';')[0].split(',')[1]];
                                console.log(result)
                            }
                        });
                    }
                }
            }, ],
			events: {
				click (e) {
					let { lng, lat } = e.lnglat
					self.ruleForm.longitude = lng
					self.ruleForm.latitude = lat
					// 这里通过高德 SDK 完成。
					/* eslint-disable */
					var geocoder = new AMap.Geocoder({
						radius: 1000,
						extensions: 'all'
					})
					geocoder.getAddress([lng, lat], function (status, result) {
						if (status === 'complete' && result.info === 'OK') {
							if (result && result.regeocode) {
                                self.mapVisiable = false
								self.ruleForm.fulladdress = result.regeocode.formattedAddress
							}
						}
					})
				}
			},
			
			ruleForm: {
				huiyimingcheng: '',
				xuehao: '',
				xingming: '',
				qiandaoshuoming: '',
				qiandaoshijian: '',
				longitude: '',
				latitude: '',
				fulladdress: '',
			},
		

			
			rules: {
